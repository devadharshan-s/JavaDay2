package Assignment;

public class LiteralsAndVariables {
    public static void main(String[] args) {
        //Literals
        int a = 25;
        float b = 3.14f;
        char c = 'c';
        String d = "hello";

        //Making 'em Variable

        a = 30;
        b = 5.63f;
        c = 'f';
        d = "world";
        System.out.println("Integer variable: " + a);
        System.out.println("Float variable: " + b);
        System.out.println("Character variable: " + c);
        System.out.println("String variable: " + d);

    }
}
